USE DATABASE CAPSTONE_GOLD_DB;

CREATE SCHEMA IF NOT EXISTS ANALYTICS;

USE SCHEMA ANALYTICS;


CREATE OR REPLACE TABLE DIMCUSTOMER
(
    CUSTOMER_KEY NUMBER AUTOINCREMENT,

    CUSTOMER_ID NUMBER,

    PREFIX STRING,

    FIRST_NAME STRING,

    LAST_NAME STRING,

    BIRTH_DATE DATE,

    MARITAL_STATUS STRING,

    GENDER STRING,

    EMAIL_ADDRESS STRING,

    ANNUAL_INCOME NUMBER,

    TOTAL_CHILDREN NUMBER,

    EDUCATION_LEVEL STRING,

    OCCUPATION STRING,

    HOME_OWNER STRING
);


CREATE OR REPLACE TABLE DIMPRODUCTCATEGORY
(
    PRODUCT_CATEGORY_KEY NUMBER AUTOINCREMENT,

    PRODUCT_CATEGORY_ID NUMBER,

    CATEGORY_NAME STRING
);


CREATE OR REPLACE TABLE DIMPRODUCTSUBCATEGORY
(
    PRODUCT_SUBCATEGORY_KEY NUMBER AUTOINCREMENT,

    PRODUCT_SUBCATEGORY_ID NUMBER,

    PRODUCT_CATEGORY_ID NUMBER,

    SUBCATEGORY_NAME STRING
);


CREATE OR REPLACE TABLE DIMPRODUCT
(
    PRODUCT_KEY NUMBER AUTOINCREMENT,

    PRODUCT_ID NUMBER,

    PRODUCT_SUBCATEGORY_ID NUMBER,

    PRODUCT_SKU STRING,

    PRODUCT_NAME STRING,

    MODEL_NAME STRING,

    PRODUCT_DESCRIPTION STRING,

    PRODUCT_COLOR STRING,

    PRODUCT_SIZE STRING,

    PRODUCT_STYLE STRING,

    PRODUCT_COST FLOAT,

    PRODUCT_PRICE FLOAT
);


CREATE OR REPLACE TABLE DIMTERRITORY
(
    TERRITORY_KEY NUMBER AUTOINCREMENT,

    TERRITORY_ID NUMBER,

    REGION STRING,

    COUNTRY STRING,

    CONTINENT STRING
);


CREATE OR REPLACE TABLE DIMCALENDAR
(
    DATE_KEY NUMBER,

    FULL_DATE DATE,

    YEAR NUMBER,

    QUARTER NUMBER,

    MONTH NUMBER,

    MONTH_NAME STRING,

    WEEK NUMBER,

    DAY NUMBER,

    DAY_NAME STRING
);



CREATE OR REPLACE TABLE FACTSALES
(
    SALES_KEY NUMBER AUTOINCREMENT,


    DATE_KEY NUMBER,

    CUSTOMER_KEY NUMBER,

    PRODUCT_KEY NUMBER,

    TERRITORY_KEY NUMBER,


    ORDER_NUMBER STRING,

    ORDER_LINE_ITEM NUMBER,


    ORDER_QUANTITY NUMBER,


    REVENUE FLOAT,

    COST FLOAT,

    PROFIT FLOAT,

    PROFIT_MARGIN FLOAT,


    RETURN_QUANTITY NUMBER,

    NET_REVENUE FLOAT
);



CREATE OR REPLACE TABLE FACTRETURNS
(
    RETURN_KEY NUMBER AUTOINCREMENT,


    DATE_KEY NUMBER,

    PRODUCT_KEY NUMBER,

    TERRITORY_KEY NUMBER,


    RETURN_QUANTITY NUMBER
);